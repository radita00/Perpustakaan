    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Peminjaman p = new Peminjaman();

        p.setIdPeminjaman(Integer.parseInt(txtIdPeminjaman.getText()));
        p.setAnggota(new Anggota().getById(Integer.parseInt(txtIdAnggota.getText())));
        p.setBuku(new Buku().getById(Integer.parseInt(txtIdBuku.getText())));
        p.setTanggalPinjam(txtTglPinjam.getText());
        p.setTanggalKembali(txtTglKembali.getText());

        p.save();
        tampilkanData();
        kosongkanForm();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtIdPeminjaman.getText());
        new Peminjaman().getById(id).delete();
        tampilkanData();
        kosongkanForm();
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tblPeminjamanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPeminjamanMouseClicked
        // TODO add your handling code here:
        int row = tblPeminjaman.getSelectedRow();
        txtIdPeminjaman.setText(tblPeminjaman.getValueAt(row, 0).toString());
        txtIdAnggota.setText(tblPeminjaman.getValueAt(row, 1).toString());
        txtIdBuku.setText(tblPeminjaman.getValueAt(row, 2).toString());
        txtTglPinjam.setText(tblPeminjaman.getValueAt(row, 3).toString());
        txtTglKembali.setText(tblPeminjaman.getValueAt(row, 4).toString());
    
    }//GEN-LAST:event_tblPeminjamanMouseClicked

