    private void kosongkanForm() {
        txtIdPeminjaman.setText("0");
        txtIdAnggota.setText("");
        txtIdBuku.setText("");
        txtTglPinjam.setText("");
        txtTglKembali.setText("");
        lblNamaAnggota.setText("Nama Anggota");
        lblJudulBuku.setText("Judul Buku");
    }

    private void tampilkanData() {
        String[] kolom = {"ID", "Nama Anggota", "Judul Buku", "Tanggal Pinjam", "Tanggal Kembali"};
        ArrayList<Peminjaman> list = new Peminjaman().getAll();

        Object[][] data = new Object[list.size()][5];

        int i = 0;
        for (Peminjaman p : list) {
            data[i][0] = p.getIdPeminjaman();
            data[i][1] = p.getAnggota().getIdAnggota();
            data[i][2] = p.getBuku().getIdBuku();
            data[i][3] = p.getTanggalPinjam();
            data[i][4] = p.getTanggalKembali();
            i++;
        }

        tblPeminjaman.setModel(new DefaultTableModel(data, kolom));
    }
